# Write a program to find intersection between two sets.
s1 = {"Pratik", "Sakshee", "Swapnil", "Radhe", "Shalendra", "Sanandan", "Kanak", "Harshita"}
s2 = {"Rakesh", "Prachi", "Shanu", "Sakshee", "Swapnil", "Radhe", "Shalendra"}
print("s1: ",s1)
print("s2: ",s2)
s3 = s1.intersection(s2)
print("Intersection between s1 and s2: ",s3)